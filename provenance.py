"""Per-finding provenance file generator.

Each call writes (or skips, idempotently) `provenance/finding-N.md`, a
journalist-readable reference showing where a finding's numbers came
from, the per-month source URLs, the FX rates applied, what each
caveat means in plain English, and any cross-source corroboration. The
file is a frozen snapshot taken at generation time — if the underlying
finding is later superseded, that older provenance file is left
in place and a fresh file is generated for the new finding id.

Two entry points:

- `generate_for_finding(finding_id)` — the canonical generator.
  Idempotent: skips if `provenance/finding-N.md` already exists. Pass
  `force=True` to regenerate (e.g. after a methodology refresh).
- The briefing-pack export() flow calls this for every finding id cited
  in the rendered brief / leads doc, then copies the generated files
  into the export bundle's `provenance/` subdir so a journalist
  forwarded only the bundle has everything they need.

CLI: `python scrape.py --finding-provenance 57378`.

Generator coverage as of 2026-05-14:
- gacc_bilateral_aggregate_yoy[_import] — full template
- Everything else — stub file noting "detailed generator pending"

Extend by adding a per-subkind renderer to `_RENDERERS`.
"""

from __future__ import annotations

import logging
import os
from datetime import date, datetime, timezone
from pathlib import Path

import psycopg2
import psycopg2.extras

import lookups

log = logging.getLogger(__name__)


PROVENANCE_DIR = Path(os.environ.get("GACC_PROVENANCE_DIR", "provenance"))


def _conn():
    return psycopg2.connect(os.environ["DATABASE_URL"])


def _provenance_path(finding_id: int) -> Path:
    return PROVENANCE_DIR / f"finding-{finding_id}.md"


def generate_for_finding(
    finding_id: int, force: bool = False, supported_only: bool = False,
) -> Path | None:
    """Generate `provenance/finding-N.md` if absent. Returns the path either way.

    `force=True` regenerates an existing file — useful after a methodology
    refresh or to pick up a supersede that landed after the file was first
    written. Default is to leave existing files alone so a journalist
    re-reading an earlier export sees what they read.

    `supported_only=True` returns None (and skips writing) when the
    finding's subkind doesn't yet have a dedicated renderer. The
    bundle-into-export flow passes this to avoid littering the export
    folder with stub files for the ~thousands of findings whose
    subkinds aren't covered yet. The CLI path leaves this False so a
    journalist asking about any finding gets at least the stub."""
    with _conn() as conn, conn.cursor(cursor_factory=psycopg2.extras.RealDictCursor) as cur:
        cur.execute(
            """
            SELECT id, kind, subkind, title, body, detail, observation_ids,
                   created_at, last_confirmed_at,
                   superseded_at, superseded_by_finding_id
              FROM findings WHERE id = %s
            """,
            (finding_id,),
        )
        finding = cur.fetchone()
    if finding is None:
        raise ValueError(
            f"No finding with id={finding_id}. Cannot generate provenance."
        )

    if supported_only and finding["subkind"] not in _RENDERERS:
        return None

    dest = _provenance_path(finding_id)
    if dest.exists() and not force:
        log.debug("provenance %s already exists; skipping", dest)
        return dest

    renderer = _RENDERERS.get(finding["subkind"], _render_stub)
    body = renderer(finding)

    dest.parent.mkdir(parents=True, exist_ok=True)
    dest.write_text(body)
    log.info("provenance: wrote %s", dest)
    return dest


